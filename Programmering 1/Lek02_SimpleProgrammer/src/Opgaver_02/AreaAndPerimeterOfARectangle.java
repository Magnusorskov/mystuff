package Opgaver_02;

public class AreaAndPerimeterOfARectangle {
    public static void main (String[] args) {
        System.out.print("The area of a rectangle with a width of 5.3 and a height of 8.6 is ");
        System.out.println(5.3 * 8.6);
        System.out.print("The perimeter of a rectangle with a width of 5.3 and a height of 8.6 is ");
        System.out.println(2 * (5.3 + 8.6));
    }
}
