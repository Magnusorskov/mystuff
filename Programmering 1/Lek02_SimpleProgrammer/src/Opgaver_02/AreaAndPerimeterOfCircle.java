package Opgaver_02;

public class AreaAndPerimeterOfCircle  {
    public static void main (String[] args) {
        System.out.print("The perimeter of a circle with a radius of 6.5 is ");
        System.out.println(2 * 6.5 * Math.PI);
        System.out.print("The area of a circle with a radius of 6.5 is ");
        System.out.println(6.5 * 6.5 * Math.PI);
    }
}
